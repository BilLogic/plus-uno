/**
 * Generate knowledge-audit.md — designer-facing verification spreadsheet.
 *
 * Source of truth: design-system/docs/knowledge-audit.json
 *
 * Usage:
 *   node scripts/generate-knowledge-audit.js
 *   node scripts/generate-knowledge-audit.js --check
 */

import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';
import { listAgentViewMarkdown } from './agent-views-paths.js';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const REPO_ROOT = path.resolve(__dirname, '..');
const DOCS_DIR = path.join(REPO_ROOT, 'design-system/docs');
const AUDIT_JSON = path.join(DOCS_DIR, 'knowledge-audit.json');
const OUT_MD = path.join(REPO_ROOT, 'design-system/figma/knowledge-audit.md');

function readJson(fp) {
  return JSON.parse(fs.readFileSync(fp, 'utf8'));
}

function listPatternFiles() {
  const dir = path.join(DOCS_DIR, 'patterns');
  if (!fs.existsSync(dir)) return [];
  return fs.readdirSync(dir).filter((f) => f.endsWith('.md')).map((f) => `docs/patterns/${f}`);
}

function mdTable(headers, rows) {
  const sep = headers.map(() => '---');
  const lines = [
    `| ${headers.join(' | ')} |`,
    `| ${sep.join(' | ')} |`,
    ...rows.map((r) => `| ${r.join(' | ')} |`),
  ];
  return lines.join('\n');
}

function yesNo(v) {
  return v ? '✅ Yes' : '⏳ No';
}

function pathRef(rel) {
  return `\`design-system/${rel}\``;
}

function generate() {
  const audit = readJson(AUDIT_JSON);
  const onDisk = listAgentViewMarkdown();
  const patterns = listPatternFiles();

  const componentRows = onDisk.map(({ name, file, abs }) => {
    const entry = audit.components?.[name] || {};
    const stat = fs.statSync(abs);
    return [
      name,
      pathRef(file),
      'yes',
      yesNo(!!entry.verified),
      entry.verifiedAt || '—',
      entry.verifiedBy || '—',
      entry.notes || '—',
      stat.mtime.toISOString().slice(0, 10),
    ];
  });

  for (const [name, entry] of Object.entries(audit.components || {})) {
    if (componentRows.some((r) => r[0] === name)) continue;
    componentRows.push([
      name,
      entry.knowledgeFile ? pathRef(entry.knowledgeFile) : '—',
      'no',
      yesNo(!!entry.verified),
      entry.verifiedAt || '—',
      entry.verifiedBy || '—',
      entry.notes || '—',
      '—',
    ]);
  }

  componentRows.sort((a, b) => a[0].localeCompare(b[0]));

  const tokenRows = Object.entries(audit.tokens || {}).map(([file, entry]) => {
    const abs = path.join(REPO_ROOT, 'design-system', file);
    const exists = fs.existsSync(abs);
    const mtime = exists ? fs.statSync(abs).mtime.toISOString().slice(0, 10) : '—';
    return [
      file,
      pathRef(file),
      exists ? 'yes' : 'no',
      yesNo(!!entry.verified),
      entry.verifiedAt || '—',
      entry.verifiedBy || '—',
      entry.notes || '—',
      mtime,
    ];
  });

  const patternRows = patterns.map((file) => {
    const abs = path.join(REPO_ROOT, 'design-system', file);
    const mtime = fs.statSync(abs).mtime.toISOString().slice(0, 10);
    return [file, pathRef(file), '—', '—', mtime];
  });

  const doc = `<!-- DO NOT EDIT BY HAND. Generated by npm run generate:agent -->
<!-- Designer workflow: edit design-system/docs/knowledge-audit.json then re-run the script -->

# PLUS Design System — Knowledge Verification Audit

Tracks agent-facing docs and whether a designer has verified them.

**Edit verification status:** [\`design-system/docs/knowledge-audit.json\`](../docs/knowledge-audit.json)

\`\`\`bash
npm run generate:agent
\`\`\`

Generated: ${new Date().toISOString().slice(0, 10)}

## Components (agent-views)

${mdTable(
  ['Component', 'Agent view', 'On disk', 'Verified', 'Verified at', 'Verified by', 'Notes', 'File updated'],
  componentRows
)}

## Tokens & mapping

${mdTable(
  ['Doc', 'Path', 'On disk', 'Verified', 'Verified at', 'Verified by', 'Notes', 'File updated'],
  tokenRows
)}

## Patterns (hand-authored)

${mdTable(['Pattern', 'Path', 'Verified', 'Notes', 'File updated'], patternRows)}

## How designers verify

1. Compare agent view or MDX against Storybook + Figma.
2. Update \`design-system/docs/knowledge-audit.json\`.
3. Run \`npm run generate:agent\`.
`;

  fs.mkdirSync(path.dirname(OUT_MD), { recursive: true });
  fs.writeFileSync(OUT_MD, doc);
  console.log(`✓ ${OUT_MD}`);
  return doc;
}

function main() {
  const check = process.argv.includes('--check');
  const next = generate();
  if (check && fs.existsSync(OUT_MD)) {
    const prev = fs.readFileSync(OUT_MD, 'utf8');
    if (prev !== next) {
      console.error('✗ knowledge-audit.md is stale. Run `npm run generate:agent`.');
      process.exit(1);
    }
    console.log('✓ knowledge-audit.md is up to date');
  }
}

main();
