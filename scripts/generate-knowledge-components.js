/**
 * Generate agent-views under design-system/agent-views/ mirroring design-system/src/.
 *
 *   components/Button/Button.md  ↔  src/components/Button/
 *   forms/Checkbox.md            ↔  src/forms/Checkbox.jsx (flat)
 *   forms/DatePicker/DatePicker.md ↔ src/forms/DatePicker/
 *
 * Usage:
 *   node scripts/generate-knowledge-components.js
 *   node scripts/generate-knowledge-components.js --skeletons
 */

import fs from 'fs';
import path from 'path';
import {
  AGENT_ROOT,
  REPO_ROOT,
  componentAgentAbs,
  componentAgentRel,
  formAgentAbs,
  formAgentRel,
  listAgentViewMarkdown,
  srcComponentDir,
  srcFormDir,
  syncAgentViewsTree,
} from './agent-views-paths.js';

const COMPONENTS_INDEX = path.join(REPO_ROOT, 'design-system/src/components/index.js');
const FORMS_INDEX = path.join(REPO_ROOT, 'design-system/src/forms/index.js');
const INVENTORY = path.join(REPO_ROOT, 'docs/context/design-system/components/inventory.md');

function extractExports(indexPath) {
  if (!fs.existsSync(indexPath)) return [];
  const content = fs.readFileSync(indexPath, 'utf8');
  const names = [];
  const re = /export\s+\{.*as\s+([a-zA-Z0-9]+)\s*\}/g;
  let m;
  while ((m = re.exec(content)) !== null) names.push(m[1]);
  return [...new Set(names)].sort();
}

function inventoryBlurb(name) {
  if (!fs.existsSync(INVENTORY)) return '';
  const content = fs.readFileSync(INVENTORY, 'utf8');
  const re = new RegExp(`### ${name}\\n([\\s\\S]*?)(?=\\n### |\\n## |$)`);
  const m = content.match(re);
  if (!m) return '';
  const purpose = m[1].match(/\*\*Purpose\*\*:\s*(.+)/);
  return purpose ? purpose[1].trim() : '';
}

function propsSourcePath(name, isForm) {
  if (isForm && srcFormDir(name) === 'forms/') {
    return `design-system/src/forms/${name}.jsx`;
  }
  const dir = isForm ? srcFormDir(name) : srcComponentDir(name);
  return `design-system/src/${dir}${name}.jsx`;
}

function writeSkeleton(name, kind) {
  const isForm = kind === 'form';
  const out = isForm ? formAgentAbs(name) : componentAgentAbs(name);
  const rel = isForm ? formAgentRel(name) : componentAgentRel(name);
  const purpose = inventoryBlurb(name) || 'See Storybook MDX and component source.';
  const importPath = isForm ? '@/forms' : '@/components';
  const srcDir = isForm ? srcFormDir(name) : srcComponentDir(name);
  const propsPath = propsSourcePath(name, isForm);

  fs.mkdirSync(path.dirname(out), { recursive: true });

  const doc = `<!-- DO NOT EDIT BY HAND. Generated by npm run generate:agent -->
<!-- verified: false — update design-system/docs/knowledge-audit.json -->

# ${name}

> **GENERATED** — add usage frontmatter to component MDX; run \`npm run generate:agent\` after edits.

## Semantic purpose

${purpose}

**Import:** \`import { ${name} } from '${importPath}';\`

**Source:** \`design-system/src/${srcDir}\` · Storybook · MDX \`figmaMeta\`

## When to use

TODO: Add \`agent.whenToUse\` to component MDX frontmatter.

## Props

See \`${propsPath}\` PropTypes and nearest \`*.stories.jsx\`.

## Examples

### Correct

\`\`\`jsx
// TODO: minimal valid example from *.stories.jsx
\`\`\`

### Incorrect

\`\`\`jsx
// TODO: anti-patterns
\`\`\`

## Related

- \`design-system/docs/discovery.md\` — route to related docs

`;

  fs.writeFileSync(out, doc);
  console.log(`  + ${rel}`);
}

function linkFor(name, kind) {
  const rel = kind === 'form' ? formAgentRel(name) : componentAgentRel(name);
  const abs = kind === 'form' ? formAgentAbs(name) : componentAgentAbs(name);
  if (fs.existsSync(abs)) return `- [${name}](${rel})`;
  return `- ${name} — see Storybook`;
}

function generateComponentIndex(components) {
  const lines = components.map((name) => linkFor(name, 'component'));
  let doc = `<!-- DO NOT EDIT BY HAND. Generated by npm run generate:agent -->\n`;
  doc += `<!-- Load when: building UI | Route: design-system/docs/discovery.md -->\n`;
  doc += `# PLUS DS — Components\n\n`;
  doc += `> **CRITICAL**: Only use components listed here. If it's not listed, it DOES NOT EXIST.\n\n`;
  doc += `Mirrors \`design-system/src/components/\`. Import via \`@/components/\`.\n\n`;
  doc += `## UI components (${components.length})\n\n`;
  doc += lines.join('\n') + '\n';
  const out = path.join(AGENT_ROOT, 'components/index.md');
  fs.mkdirSync(path.dirname(out), { recursive: true });
  fs.writeFileSync(out, doc);
  console.log(`✓ ${out}`);
}

function generateFormsIndex(forms) {
  const lines = forms.map((name) => linkFor(name, 'form'));
  let doc = `<!-- DO NOT EDIT BY HAND. Generated by npm run generate:agent -->\n`;
  doc += `<!-- Load when: building forms | Route: design-system/docs/discovery.md -->\n`;
  doc += `# PLUS DS — Forms\n\n`;
  doc += `> **CRITICAL**: Only use form components listed here.\n\n`;
  doc += `Mirrors \`design-system/src/forms/\`. Import via \`@/forms/\`.\n\n`;
  doc += `## Form elements (${forms.length})\n\n`;
  doc += lines.join('\n') + '\n\n';
  doc += `\`\`\`\n${forms.join('\n')}\n\`\`\`\n`;
  const out = path.join(AGENT_ROOT, 'forms/index.md');
  fs.mkdirSync(path.dirname(out), { recursive: true });
  fs.writeFileSync(out, doc);
  console.log(`✓ ${out}`);
}

function writeReadme() {
  const doc = `# DO NOT EDIT BY HAND — generated by npm run generate:agent

Agent-readable views mirroring \`design-system/src/\` folder names and layout.

| Mirrored | Notes |
|----------|-------|
| \`components/\` | Per-component \`{Name}/{Name}.md\` skeletons |
| \`forms/\` | Flat \`{Name}.md\` or \`{Name}/{Name}.md\` for subdirs |
| \`tokens/\` | Generated token list from \`src/tokens/*.scss\` |
| \`styles/\`, \`specs/\`, \`DataViz/\` | Directory tree only (content from source when needed) |

**Excluded** (not DS knowledge for agents): \`storybook-docs/\` (Storybook layout primitives), \`assets/\` (static media).

Regenerate: \`npm run generate:agent\`
`;
  fs.writeFileSync(path.join(AGENT_ROOT, 'README.md'), doc);
}

function main() {
  const withSkeletons = process.argv.includes('--skeletons');
  console.log('Syncing agent-views directory tree from design-system/src...');
  const dirs = syncAgentViewsTree();
  console.log(`  ✓ mirrored ${dirs} directories (excludes storybook-docs, assets)`);

  const components = extractExports(COMPONENTS_INDEX);
  const forms = extractExports(FORMS_INDEX);

  if (withSkeletons) {
    console.log('Writing per-component skeletons...');
    for (const name of components) writeSkeleton(name, 'component');
    for (const name of forms) {
      if (!components.includes(name)) writeSkeleton(name, 'form');
    }
  }

  generateComponentIndex(components);
  generateFormsIndex(forms);
  writeReadme();

  const onDisk = listAgentViewMarkdown().length;
  console.log(`Done. ${onDisk} component/form agent views on disk.`);
}

main();
