/**
 * Generate design-system/agent-views/components/index.md
 * and per-component skeleton docs from source exports + inventory.
 *
 * Usage:
 *   node scripts/generate-knowledge-components.js
 *   node scripts/generate-knowledge-components.js --skeletons
 */

import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const REPO_ROOT = path.resolve(__dirname, '..');
const AGENT_COMPONENTS = path.join(REPO_ROOT, 'design-system/agent-views/components');
const COMPONENTS_INDEX = path.join(REPO_ROOT, 'design-system/src/components/index.js');
const FORMS_INDEX = path.join(REPO_ROOT, 'design-system/src/forms/index.js');
const INVENTORY = path.join(REPO_ROOT, 'docs/context/design-system/components/inventory.md');
const OUT_INDEX = path.join(AGENT_COMPONENTS, 'index.md');

function extractExports(indexPath) {
  if (!fs.existsSync(indexPath)) return [];
  const content = fs.readFileSync(indexPath, 'utf8');
  const names = [];
  const re = /export\s+\{.*as\s+([a-zA-Z0-9]+)\s*\}/g;
  let m;
  while ((m = re.exec(content)) !== null) names.push(m[1]);
  return [...new Set(names)].sort();
}

function slugFor(name) {
  return name.replace(/([a-z])([A-Z])/g, '$1-$2').toLowerCase();
}

function hasDetailDoc(name) {
  const slug = slugFor(name);
  return fs.existsSync(path.join(AGENT_COMPONENTS, `${slug}.md`));
}

function inventoryBlurb(name) {
  if (!fs.existsSync(INVENTORY)) return '';
  const content = fs.readFileSync(INVENTORY, 'utf8');
  const re = new RegExp(`### ${name}\\n([\\s\\S]*?)(?=\\n### |\\n## |$)`);
  const m = content.match(re);
  if (!m) return '';
  const purpose = m[1].match(/\*\*Purpose\*\*:\s*(.+)/);
  return purpose ? purpose[1].trim() : '';
}

function writeSkeleton(name) {
  const slug = slugFor(name);
  const out = path.join(AGENT_COMPONENTS, `${slug}.md`);

  const purpose = inventoryBlurb(name) || 'See Storybook MDX and component source.';
  const isForm = ['Input', 'Select', 'Checkbox', 'Textarea', 'Switch'].includes(name);
  const importPath = isForm ? `@/forms` : `@/components`;
  const srcDir = isForm ? 'forms' : 'components';

  const doc = `<!-- DO NOT EDIT BY HAND. Generated by npm run generate:agent-views -->
<!-- verified: false — update design-system/docs/knowledge-audit.json -->

# ${name}

> **GENERATED** — add usage frontmatter to component MDX; run \`npm run generate:agent\` after edits.

## Semantic purpose

${purpose}

**Import:** \`import { ${name} } from '${importPath}';\`

**Source:** \`design-system/src/${srcDir}/${name}/\` · Storybook · MDX \`figmaMeta\`

## When to use

TODO: Add \`agent.whenToUse\` to component MDX frontmatter.

## Props

See \`design-system/src/${srcDir}/${name}/${name}.jsx\` PropTypes and nearest \`*.stories.jsx\`.

## Examples

### Correct

\`\`\`jsx
// TODO: minimal valid example from *.stories.jsx
\`\`\`

### Incorrect

\`\`\`jsx
// TODO: anti-patterns
\`\`\`

## Related

- \`design-system/docs/discovery.md\` — route to related docs

`;

  fs.writeFileSync(out, doc);
  console.log(`  + ${slug}.md`);
}

function generateIndex(components, forms) {
  const ui = components.map((name) => {
    const slug = slugFor(name);
    if (hasDetailDoc(name)) {
      return `- [${name}](${slug}.md)`;
    }
    return `- ${name} — see Storybook`;
  });

  let doc = `<!-- DO NOT EDIT BY HAND. Generated by npm run generate:agent-views -->\n`;
  doc += `<!-- Load when: building UI | Route: design-system/docs/discovery.md -->\n`;
  doc += `# PLUS DS — Components\n\n`;
  doc += `> **CRITICAL**: Only use components listed here. If it's not listed, it DOES NOT EXIST.\n\n`;
  doc += `Import UI via \`@/components/\`. Import forms via \`@/forms/\` — see [form.md](form.md).\n\n`;
  doc += `## UI components (${components.length})\n\n`;
  doc += ui.join('\n') + '\n\n';
  doc += `## Form elements\n\n`;
  doc += `See [form.md](form.md):\n\n\`\`\`\n${forms.join('\n')}\n\`\`\`\n`;

  fs.writeFileSync(OUT_INDEX, doc);
  console.log(`✓ ${OUT_INDEX}`);
}

function main() {
  const withSkeletons = process.argv.includes('--skeletons');
  console.log('Generating agent component views...');
  fs.mkdirSync(AGENT_COMPONENTS, { recursive: true });

  const components = extractExports(COMPONENTS_INDEX);
  const forms = extractExports(FORMS_INDEX);

  if (withSkeletons) {
    console.log('Writing per-component skeletons...');
    for (const name of components) writeSkeleton(name);
    for (const name of forms) {
      if (!components.includes(name)) writeSkeleton(name);
    }
  }

  generateIndex(components, forms);
  console.log('Done.');
}

main();
